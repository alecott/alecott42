#include "../printf.h"

void	ft_putnbr(int n)
{
	long int	i;

	i = (long int)n;
	if (i < 0)
	{
		i = i * -1;
		ft_putchar('-');
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
	}
	ft_putchar(i % 10 + '0');
}
