#include "../printf.h"

int		ft_putwstr(wchar_t const *s, int j)
{
	int		i;

	i = 0;
	if (s == NULL)
		return (j);
	while (s[i])
	{
		j = ft_putwchar(s[i], j);
		i++;
	}
	return (j);
}
